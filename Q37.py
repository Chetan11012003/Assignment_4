# write a python script to calculate average of three number, entered by the user.

num_1 = eval(input("Enter a First number:- "))
num_2 = eval(input("Enter a Second number:- "))
num_3 = eval(input("Enter a third number:- "))

average = (num_1+num_2+num_3)/3

print("The average of three number is:- ",average)